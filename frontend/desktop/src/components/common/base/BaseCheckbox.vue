<template>
    <div class="base-checkbox" @click.stop="onCheck">
        <span :class="['checkbox', { checked: checked }]">
        </span>
        <slot></slot>
    </div>
</template>
<script>
    import '@/utils/i18n.js'
    export default {
        name: 'BaseCheckbox',
        props: ['isChecked'],
        data () {
            return {
                checked: this.isChecked || false
            }
        },
        watch: {
            isChecked (val) {
                this.checked = val
            }
        },
        methods: {
            onCheck () {
                this.checked = !this.checked
                this.$emit('checkCallback', this.checked)
            }
        }
    }
</script>
