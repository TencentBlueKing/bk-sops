                            'permission-disable': !hasPermission(['mini_app_edit'], appData.auth_actions)
                        }]"
                        :title="$t('修改轻应用')"
                        v-cursor="{ active: !hasPermission(['mini_app_edit'], appData.auth_actions) }"
                        data-test-id="appmaker_form_editBtn"
                        @click.stop="onCardEdit">
                    </span>
                    <router-link
